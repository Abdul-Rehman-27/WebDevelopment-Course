// Operator Precedence
/**
Operator precedence means the order in which JavaScript evaluates operators in an expression (just like BODMAS in maths). 
**/

// Common Operator Precedence
/**  
Highest->Lowest 
1-) ()
2-) ++ -- **
3-) * / %  (left to right)
4-) + -
5-) < > <= >=
6-) == != === !==
7-) &&
8-) `
9-) =
**/

