// 1-Basic Assignment =
let x = 2;
console.log(x);

// 2-Addition Assignment += 
let age = 20;
age += 1;
console.log(age);

//3-Subtraction Assignment -=
let num = 27;
num -= 1;
console.log(num)

// 4-Multiplication Assignment *=
let a = 6;
a *= 2;   // a = a * 2
console.log(a); // 12

// 5-Division Assignment /=
let number = 20;
number /= 4;   // number = number / 4
console.log(number); // 5

// 6-Modulus Assignment %=
let num1 = 10;
num1 %= 3;   // num1 = num1 % 3
console.log(num1); // 1

// 7-Exponentiation Assignment **=
let a1 = 2;
a1 **= 3;  // a1 = a1 ** 3
console.log(a1); // 8


