// --------------Unary Operators---------------
// 1-Post Increment
let num = 1;
num++;
console.log(num);

// 2-Pre Inrement
let num1 = 2;
++num1;
console.log(num1);

// 3-Post Decrement
let number = 3;
number--;
console.log(number)

// 4-Pre Decrement
let number1 = 4;
--number1;
console.log(number1);