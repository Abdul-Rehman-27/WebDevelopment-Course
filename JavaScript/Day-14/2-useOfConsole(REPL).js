// REPL (Read-Evaluate-Print-Loop)
/**
REPL in the console means Read–Eval–Print Loop.it’s an interactive console where you:

-Read → type a command
-Eval → the system executes it
-Print → shows the result
-Loop → waits for the next command

This cycle keeps repeating.**/

/** 
> 2 + 3
5
> let x = 10
undefined
> x * 2
20
**/