//-------------While Loop---------------
/** 
-A while loop repeats a block of code as long as a condition is true.
**/

//-Syntax
// while (condition) {
//   // code to be executed
// }

//-Example (Forward)
console.log("---------Forward While loop-----------------");
let i = 1;

while (i <= 5) {
    console.log(i);
    i++;
}

//-Example (Backward)
console.log("----------Backward While loop---------------");
let j = 5;

while (j >= 1) {
    console.log(j);
    j--;
}
