//-------------------for loops------------------------
/** 
-A for loop is used when you already know how many times you want to run a block of code.
**/

//-Syntax
//for (initialization; condition; increment/decrement) {
    // code to run
//}

//-Example (forward)
console.log("------Forward For Loop----------")
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

//-Example (backward)
console.log("------BackwardFor Loop----------")
for (let i = 5; i >= 1; i--) {
    console.log(i);
}

