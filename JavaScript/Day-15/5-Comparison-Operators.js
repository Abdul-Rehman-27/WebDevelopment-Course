// --------------Comparison Operators---------

//1. Equal to (==)
let a1 = 10;
let b1 = "10";
console.log(a1 == b1); // true

//2️. Strict Equal to (===)
let a2 = 10;
let b2 = "10";
console.log(a2 === b2); // false

//3. Not Equal to (!=)
let a3 = 5;
let b3 = "5";
console.log(a3 != b3); // false

//4. Strict Not Equal to (!==)
let a4 = 5;
let b4 = "5";
console.log(a4 !== b4); // true

//5. Greater Than (>)
let x1 = 20;
let y1 = 10;
console.log(x1 > y1); // true

//6. Less Than (<)
let x2 = 5;
let y2 = 15;
console.log(x2 < y2); // true

//7. Greater Than or Equal to (>=)
let p1 = 10;
let q1 = 10;
console.log(p1 >= q1); // true

//8. Less Than or Equal to (<=)
let p2 = 8;
let q2 = 10;
console.log(p2 <= q2); // true
