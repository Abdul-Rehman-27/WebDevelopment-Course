// Q: Create a system to calulate popcorn price based on the size customer asked for

let size = "XL";
if (size == "XL") {
    console.log("Price: 250 Rupees");
}else if (size == "L") {
    console.log("Price: 200 Rupees");
}else if (size == "M") {
    console.log("Price: 100 Rupees");
}else if (size == "S") {
    console.log("Price: 50 Rupees");
}