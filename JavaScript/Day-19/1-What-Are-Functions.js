//-------------Function in Js----------------
/** 
A function in JavaScript is a block of code that performs a specific task.
You can reuse it multiple times, which makes your code cleaner and easier to manage.
**/

//-Syntax
function sayHello() {
  console.log("Hello!");
}
sayHello();

function print1to5() {
    for (let i=1; i<=5; i++) {
        console.log(i);
    }
}
print1to5();
