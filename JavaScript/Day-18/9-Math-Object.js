//-------------Math Object in JS------------------
/** 
In JavaScript, Math is a built-in object that provides many mathematical functions and constants.
**/
console.log(Math.PI);      // return 3.14
console.log(-27);          // return 27
console.log(Math.pow(2,3)); // return 8
console.log(Math.floor(5.8)); // return 5 
console.log(Math.floor(-5.5)); // return -6  
console.log(Math.ceil(2.7)); // return 3
console.log(Math.ceil(-5.5)); // return -5
console.log(Math.random()); // give value from 0 to 1 but 1 is excluded